package wrappers;

public class StringWrapper {
	
	private String string;

	public StringWrapper(String string) {
		super();
		this.string = string;
	}

	public StringWrapper() {
		super();
	}

	public String getString() {
		return string;
	}

	public void setString(String string) {
		this.string = string;
	}
	
	

}
